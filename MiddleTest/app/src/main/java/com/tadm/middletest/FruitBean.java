package com.tadm.middletest;

public class FruitBean {
    private String fruitName;
    private int fruitImage;
    private String fruitPrice;
    private String fruitShore;

    public String getFruitName() {
        return fruitName;
    }

    public void setFruitName(String fruitName) {
        this.fruitName = fruitName;
    }

    public int getFruitImage() {
        return fruitImage;
    }

    public void setFruitImage(int fruitImage) {
        this.fruitImage = fruitImage;
    }

    public String getFruitPrice() {
        return fruitPrice;
    }

    public void setFruitPrice(String fruitPrice) {
        this.fruitPrice = fruitPrice;
    }

    public String getFruitShore() {
        return fruitShore;
    }

    public void setFruitShore(String fruitShore) {
        this.fruitShore = fruitShore;
    }
}
